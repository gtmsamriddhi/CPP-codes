#include <iostream>
using namespace std;

int main() {
    cout << "Even numbers from 1 to 100:" << endl;
    for (int i = 1; i <= 100; i++) {
        if (i % 2 == 0)
            cout << i << " ";
    }
    cout << endl;
    cout << "Samriddhi Gautam : BIT 28 : Even numbers using loop" ;
    return 0;
}
